async function uploadFileToBox(
  token: string,
  folderId: string,
  fileName: string,
  content: string,
): Promise<{ name: string; id?: string }> {
  const form = new FormData();
  form.append(
    "attributes",
    JSON.stringify({ name: fileName, parent: { id: folderId } }),
  );
  form.append("file", new Blob([content], { type: "text/markdown" }), fileName);

  const res = await fetch("https://upload.box.com/api/2.0/files/content", {
    method: "POST",
    headers: { Authorization: `Bearer ${token}` },
    body: form,
  });

  if (!res.ok) {
    const text = await res.text().catch(() => "");
    throw new Error(
      `Box upload failed for ${fileName} (${res.status}): ${text.slice(0, 300)}`,
    );
  }

  const data = (await res.json()) as { entries?: Array<{ id: string; name: string }> };
  const entry = data.entries?.[0];
  return { name: entry?.name ?? fileName, id: entry?.id };
}

export const saveReportToBox = createServerFn({ method: "POST" }).handler(
  async () => {
    const token = process.env.BOX_ACCESS_TOKEN;
    const folderId = process.env.BOX_FOLDER_ID;
    if (!token) throw new Error("BOX_ACCESS_TOKEN is not configured.");
    if (!folderId || !/^\d+$/.test(folderId)) {
      throw new Error(
        "Box folder ID is missing or invalid. Please use the numeric folder ID from the Box folder URL.",
      );
    }

    const uploaded: Array<{ name: string; id?: string }> = [];
    for (const f of REPORT_FILES) {
      uploaded.push(await uploadFileToBox(token, folderId, f.name, f.content));
    }

    return {
      status: "saved" as const,
      destination: "Box",
      folder_id: folderId,
      uploaded_files: uploaded,
    };
  },
);
