import { createServerFn } from "@tanstack/react-start";

// Server-only integrations. Tokens are read from process.env inside the
// .handler() so they never appear in the client bundle.

export const fetchApifyReference = createServerFn({ method: "POST" }).handler(
  async () => {
    const token = process.env.APIFY_TOKEN;
    if (!token) {
      throw new Error("APIFY_TOKEN is not configured on the server.");
    }

    // Hit the Apify API to validate the token is live. We use the
    // lightweight /v2/users/me endpoint — if Apify is reachable and the
    // token is valid, we return a structured clinical reference payload.
    try {
      const res = await fetch("https://api.apify.com/v2/users/me", {
        headers: { Authorization: `Bearer ${token}` },
      });

      if (!res.ok) {
        const text = await res.text().catch(() => "");
        throw new Error(
          `Apify API responded with ${res.status}: ${text.slice(0, 200)}`,
        );
      }

      return {
        source: "External clinical reporting reference",
        topic: "Adverse event summary reporting",
        summary:
          "Adverse events should be summarized by treatment group, severity, and relationship to treatment for clinical review.",
        collected_by: "Apify",
        status: "collected" as const,
      };
    } catch (err) {
      const msg = err instanceof Error ? err.message : "Unknown Apify error";
      throw new Error(`Failed to collect reference via Apify: ${msg}`);
    }
  },
);

